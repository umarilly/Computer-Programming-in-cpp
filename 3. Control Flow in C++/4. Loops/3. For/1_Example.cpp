
/*
    Program in C++ to demonstrate the use of for loop.
*/

#include <iostream>
#include <conio.h>

using namespace std;

int main() {
    
    int a; // variable declaration

    // for loop to print numbers from 1 to 10

    for (a = 1; a <= 10; a++) {
        // This block of code will be executed 10 times
            cout << "a : " << a << endl;
        }

    _getch();
    return 0;
}